class Card {
  constructor({title, poster, description, rating, year, duration, genre, commentsQuantity}) {
    this._title = title;
    this._poster = poster;
    this._description = description;
    this._rating = rating;
    this._year = year;
    this._duration = duration;
    this._genre = genre;
    this._commentsQuantity = commentsQuantity;
  }
}
